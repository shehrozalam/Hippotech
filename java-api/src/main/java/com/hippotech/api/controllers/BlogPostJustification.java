package com.hippotech.api.controllers;

public class BlogPostJustification {
    private String post;
    private String justification;

    public String getPost() {
      return post;
    }
    public String getJustification() {
        return justification;
    }

    public void setPost(String error) {
        this.post = error;
    }

    public void setJustification(String word) {
        this.justification = word;
    }
}
